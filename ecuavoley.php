                       <h2>SELECCIÓN DE ECUAVOLEY</h2>
                       <P>
                        La Selección de Ecuavoley es otro de los grupos deportivos destacados de esta institución. 
                        En abril de 2023 se consagraron campeones del Campeonato Intercolegial de Ecuavoley de la 
                        Federación Deportiva Estudiantil.
                       <P>
                        En el Colegio Simón Bolívar, el ecuavoley es una actividad deportiva destacada que fomenta 
                        la participación y el desarrollo físico de los estudiantes. Este deporte, 
                        adaptado de las tradiciones ecuatorianas, se juega con equipos de tres jugadores cada uno 
                        y utiliza una cancha similar a la de voleibol pero con una red más alta. Las reglas 
                        particulares del ecuavoley, que permiten un mayor contacto con la pelota y un estilo de 
                        juego más dinámico, hacen que sea un deporte accesible y emocionante para los estudiantes.
                       </P>
                       <P>
                        El ecuavoley no solo promueve la actividad física, sino que también inculca valores esenciales
                         como el trabajo en equipo, la disciplina y el respeto por los demás. A través de torneos 
                         internos y actividades extracurriculares, los alumnos de la Unidad Educativa Fiscal "Simón Bolívar" tienen la 
                         oportunidad de mejorar sus habilidades técnicas y fortalecer su condición física.
                       </P>
                       <P>
                        Esta práctica deportiva se integra en el programa educativo del colegio, apoyando el bienestar 
                        integral de los estudiantes y proporcionando un espacio para la recreación y la socialización.
                         Así, el ecuavoley en la Institucion se convierte en una herramienta educativa que 
                         contribuye al desarrollo personal y comunitario de sus alumnos.
